package com.konloch.constants;

/**
 * @author Konloch
 * @since 12/28/2020
 */
public class INT
{
	/**
	 * Represents the numerical value one
	 */
	public static final int ONE = 1;
	
	/**
	 * Represents the numerical value ten
	 */
	public static final int TEN = ONE * 10;
	
	/**
	 * Represents the numerical value one hundred
	 */
	public static final int HUNDRED = TEN * 10;
	
	/**
	 *
	 * Represents the numerical value one thousand
	 */
	public static final int THOUSAND = HUNDRED * 10;
	
	/**
	 * Represents the numerical value ten hundred
	 */
	public static final int TEN_THOUSAND = THOUSAND * 10;
	
	/**
	 * Represents the numerical value one hundred thousand
	 */
	public static final int HUNDRED_THOUSAND = TEN_THOUSAND * 10;
	
	/**
	 * Represents the numerical value one million
	 */
	public static final int MILLION = HUNDRED_THOUSAND * 10;
	
	/**
	 * Represents the numerical value ten million
	 */
	public static final int TEN_MILLION = MILLION * 10;
	
	/**
	 * Represents the numerical value one hundred million
	 */
	public static final int HUNDRED_MILLION = TEN_MILLION * 10;
	
	/**
	 * Represents the numerical value one billion
	 */
	public static final int BILLION = HUNDRED_MILLION * 10;
}
